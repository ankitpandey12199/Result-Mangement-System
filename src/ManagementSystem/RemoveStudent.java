    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        String rollNo = Roll.getText();
        if (rollNo.length() == 0) {
            JOptionPane.showMessageDialog(null, "SEARCH FOR ROLL NO FIRST:");

        }
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("Select * from student where rollNo='" + rollNo + "'");
            if (!rs.first()) {
                JOptionPane.showMessageDialog(null, "roll no doesn't extis,try again!!");
            } else {
                Roll.setEditable(false);
                name.setText(rs.getString(1));
                name.setEditable(false);
                roll.setText(rs.getString(2));
                name.setEditable(false);
                gender.setSelectedItem(rs.getString(3));
                gender.setEditable(false);
                fname.setText(rs.getString(4));
                fname.setEditable(false);
                course.setSelectedItem(rs.getString(5));
                course.setEditable(false);
                branch.setSelectedItem(rs.getString(6));
                branch.setEditable(false);

            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());

        }

    }//GEN-LAST:event_SearchActionPerformed

