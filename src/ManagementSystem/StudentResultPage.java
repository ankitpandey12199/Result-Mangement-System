    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown

        String rollNo = RollNo.getText();
        try {
             Connection con=ConnectionProvider.getCon();
             Statement st=con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE,ResultSet.CONCUR_UPDATABLE);
             ResultSet rs=st.executeQuery("Select * from student Inner JOin result where student.rollNo='"+rollNo+"' And result.rollNo='"+rollNo+"'");
             if(!rs.first())
             {
                 JOptionPane.showMessageDialog(null, "Roll no doesnt exits");
             }
             else
             {
                 Name.setText(rs.getString(1));
                 Gender.setText(rs.getString(3));
                 FName.setText(rs.getString(4));
                 Course.setText(rs.getString(5));
                 Branch.setText(rs.getString(6));
                 Math.setText(rs.getString(8));
                 Che.setText(rs.getString(9));
                 Physics.setText(rs.getString(10));
                 English.setText(rs.getString(11));
                 Elec.setText(rs.getString(12));
                 TotalMarks.setText(rs.getString(13));
                 if(rs.getInt(8)<33 || rs.getInt(13)/5<33 ||rs.getInt(9)<33 ||rs.getInt(10)<33 ||rs.getInt(11)<33 ||rs.getInt(12)<33)
                     Verdict.setText("FAIL");
                 else
                     Verdict.setText("PASS");
                 Name.setEditable(false);
                 Gender.setEditable(false);
                 FName.setEditable(false);
                 Course.setEditable(false);
                 Branch.setEditable(false);
                 Math.setEditable(false);
                 Che.setEditable(false);
                 Physics.setEditable(false);
                 English.setEditable(false);
                 Elec.setEditable(false);
                 TotalMarks.setEditable(false);
                     Verdict.setEditable(false);
             }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_formComponentShown

