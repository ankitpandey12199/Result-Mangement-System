    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        String rollNo = roll.getText();
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("Select * from student where rollNo='" + rollNo + "'");
            details.setModel(DbUtils.resultSetToTableModel(rs));
            if (!rs.first()) {
                JOptionPane.showMessageDialog(null, "roll no not found,try again");
                this.setVisible(false);
                AddResult frame = new AddResult();
                frame.setVisible(true);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_SearchActionPerformed

    private void SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubmitActionPerformed
        // TODO add your handling code here:
        String Roll = roll.getText();
        if (Roll.length() == 0) {
            JOptionPane.showMessageDialog(null, "PLEASE SEARCH ROLL NO BEFORE SUBMITTING,TRY AGAIN");
            Clear.doClick();
            return;
        }
        int math = Integer.parseInt(Math.getText());
        int che = Integer.parseInt(CHE.getText());
        int phy = Integer.parseInt(PHYSICS.getText());
        int english = Integer.parseInt(ENGLISH.getText());
        int elec = Integer.parseInt(ELECTRICAL.getText());
        if (math > 100 || math < 0) {
            JOptionPane.showMessageDialog(null, "Invalid Math marks");
            Clear.doClick();
            return;
        }
        if (che > 100 || che < 0) {
            JOptionPane.showMessageDialog(null, "Invalid Chemistry marks");
            Clear.doClick();return;
        }
        if (phy > 100 || phy < 0) {
            JOptionPane.showMessageDialog(null, "Invalid Physics marks");
            Clear.doClick();
            return;
        }
        if (english > 100 || english < 0) {
            JOptionPane.showMessageDialog(null, "Invalid English marks");
            Clear.doClick();return;
        }
        if (elec > 100 || elec < 0) {
            JOptionPane.showMessageDialog(null, "Invalid Electrical marks");
            Clear.doClick();
            return;
        }

        try {
            java.sql.Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("insert into result values('" + Roll + "','" + math + "','" + che + "','" + phy + "','" + english + "','" + elec + "','" + (math + phy + che + elec + english) + "');");
            JOptionPane.showMessageDialog(null, "Sucessfully added.");
            this.setVisible(false);
            AddResult frame = new AddResult();
            frame.setVisible(true);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
            Clear.doClick();

        }
    }//GEN-LAST:event_SubmitActionPerformed

