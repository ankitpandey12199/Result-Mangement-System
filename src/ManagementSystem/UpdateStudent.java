    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        int math = Integer.parseInt(Math.getText());
        int che = Integer.parseInt(Che.getText());
        int phy = Integer.parseInt(Physics.getText());
        int english = Integer.parseInt(English.getText());
        int elec = Integer.parseInt(Elec.getText());
        int total = math + che + phy + english + elec;
        if (math > 100 || math < 0) {
            JOptionPane.showMessageDialog(null, "Invalid Math marks");
            Clear.doClick();
            return;
        }
        if (che > 100 || che < 0) {
            JOptionPane.showMessageDialog(null, "Invalid Chemistry marks");
            Clear.doClick();return;
        }
        if (phy > 100 || phy < 0) {
            JOptionPane.showMessageDialog(null, "Invalid Physics marks");
            Clear.doClick();
            return;
        }
        if (english > 100 || english < 0) {
            JOptionPane.showMessageDialog(null, "Invalid English marks");
            Clear.doClick();return;
        }
        if (elec > 100 || elec < 0) {
            JOptionPane.showMessageDialog(null, "Invalid Electrical marks");
            Clear.doClick();
            return;
        }
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            st.executeUpdate("UPDATE result set Math=" + math + ",Chemistry=" + che + ",Physics=" + phy + ",English=" + english + ",Electrical=" + elec + ",TOTAL=" + total + " Where rollNo='" + Roll.getText() + "'");
            JOptionPane.showMessageDialog(null, "Updated Successfully");
            Clear.doClick();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_UpdateActionPerformed

