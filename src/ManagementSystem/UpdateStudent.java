    private void SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SearchActionPerformed
        // TODO add your handling code here:
        String rollNo = Roll.getText();
        if (rollNo.length() == 0) {
            JOptionPane.showMessageDialog(null, "SEARCH FOR ROLL NO FIRST:");
            Clear.doClick();
        }
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement(ResultSet.TYPE_SCROLL_SENSITIVE, ResultSet.CONCUR_UPDATABLE);
            ResultSet rs = st.executeQuery("Select * from result where rollNo='" + rollNo + "'");
            if (!rs.first()) {
                JOptionPane.showMessageDialog(null, "roll no doesn't extis,try again!!");
                Clear.doClick();
            } else {
                Roll.setEditable(false);
                Math.setText(rs.getString(2));
                Che.setText(rs.getString(3));
                Physics.setText(rs.getString(4));
                English.setText(rs.getString(5));
                Elec.setText(rs.getString(6));
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.toString());
            Clear.doClick();
        }
    }//GEN-LAST:event_SearchActionPerformed

